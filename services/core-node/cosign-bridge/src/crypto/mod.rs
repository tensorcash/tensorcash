    #[test]
    fn test_sas_from_handshake_hash_consistency() {
        // Test that both parties derive the same SAS after completing Noise handshake
        let mut initiator = CryptoSession::new("test-invite-code").unwrap();
        let mut responder = CryptoSession::new("test-invite-code").unwrap();

        // Complete SPAKE2 exchange
        let init_msg = initiator.spake2_start(true).unwrap();
        let resp_msg = responder.spake2_start(false).unwrap();
        initiator.spake2_finish(true, &resp_msg).unwrap();
        responder.spake2_finish(false, &init_msg).unwrap();

        // Initialize Noise
        initiator.init_noise(true).unwrap();
        responder.init_noise(false).unwrap();

        // Complete Noise handshake
        let msg1 = initiator.noise_handshake_write().unwrap();
        responder.noise_handshake_step(&msg1).unwrap();
        let msg2 = responder.noise_handshake_write().unwrap();
        initiator.noise_handshake_step(&msg2).unwrap();

        // Both should have handshake hash now
        assert!(initiator.handshake_hash.is_some());
        assert!(responder.handshake_hash.is_some());

        // Both should derive the SAME SAS from handshake hash
        let init_sas = initiator.generate_sas("session-id");
        let resp_sas = responder.generate_sas("session-id");

        assert_eq!(
            init_sas, resp_sas,
            "SAS should match after handshake completion"
        );

        // SAS should be 5 words separated by dashes
        let word_count = init_sas.split('-').count();
        assert_eq!(word_count, 5, "SAS should have 5 words");

        // All words should be from EFF wordlist (non-empty, lowercase)
        for word in init_sas.split('-') {
            assert!(!word.is_empty());
            assert!(word.chars().all(|c| c.is_ascii_lowercase()));
        }
    }

