/// Derive Noise PSK from SPAKE2 shared secret using HKDF-SHA256
/// This provides proper key hygiene by domain-separating the derived key
fn derive_noise_psk(shared_secret: &[u8]) -> Result<Vec<u8>> {
    let hkdf = Hkdf::<Sha256>::new(None, shared_secret);
    let mut psk = vec![0u8; 32]; // 32 bytes for Noise PSK
    hkdf.expand(b"cosign-noise-psk-v1", &mut psk)
        .map_err(|_| anyhow::anyhow!("HKDF expansion failed"))?;
    Ok(psk)
}

/// Derive SAS from Noise handshake hash
/// Returns 5 words from EFF wordlist (55 bits of entropy)
fn derive_sas_from_transcript(handshake_hash: &[u8]) -> String {
    // Hash the handshake_hash with SHA256 for additional mixing
    let mut hasher = Sha256::new();
    hasher.update(handshake_hash);
    let hash = hasher.finalize();

    // Extract 5 word indices (11 bits each = 2048 words per index)
    // Total entropy: 55 bits
    // We use a simpler approach: take consecutive bytes and map to word indices
    let mut words = Vec::with_capacity(5);

    for i in 0..5 {
        // Each word needs 11 bits for 2048 word space
        // Use 2 bytes (16 bits) per word for simplicity
        let byte_idx = i * 2;

        if byte_idx + 1 < hash.len() {
            // Read 2 bytes as u16
            let two_bytes = u16::from_be_bytes([hash[byte_idx], hash[byte_idx + 1]]);
            // Take lower 11 bits (0x7FF = 0b11111111111)
            let word_idx = (two_bytes & 0x7FF) as usize;
            // Map to wordlist with modulo for safety
            let final_idx = word_idx % EFF_WORDLIST.len().min(2048);
            words.push(EFF_WORDLIST[final_idx]);
        } else {
            // Fallback: use single byte with modulo
            let word_idx = (hash[byte_idx % hash.len()] as usize) % EFF_WORDLIST.len();
            words.push(EFF_WORDLIST[word_idx]);
        }
    }

    words.join("-")
}

// Wrapper to store SPAKE2 state with proper lifetimes
enum Spake2StateWrapper {
    Initiator(spake2::Spake2<Ed25519Group>),
    Responder(spake2::Spake2<Ed25519Group>),
}

pub struct CryptoSession {
    invite_code: String,
    #[allow(dead_code)]
    shared_secret: Option<Vec<u8>>,
    #[allow(dead_code)]
    noise_state: Option<NoiseState>,
    // Store SPAKE2 state for two-phase exchange
    spake2_state: Option<Spake2StateWrapper>,
    /// Handshake hash from Noise protocol (for SAS derivation)
    handshake_hash: Option<Vec<u8>>,
}

#[allow(dead_code)]
enum NoiseState {
    Handshake(Box<HandshakeState>),
    Transport(Box<TransportState>),
}

impl CryptoSession {
    /// Create a new crypto session with the given invite code (PAKE password)
    pub fn new(invite_code: &str) -> Result<Self> {
        Ok(Self {
            invite_code: invite_code.to_string(),
            shared_secret: None,
            noise_state: None,
            spake2_state: None,
            handshake_hash: None,
        })
    }

    /// Start SPAKE2 exchange and return outbound message
    /// Call this once to get the message to send to peer
    #[allow(dead_code)]
    pub fn spake2_start(&mut self, is_initiator: bool) -> Result<Vec<u8>> {
        log::debug!("Starting SPAKE2 exchange (initiator: {})", is_initiator);

        // Use the invite code as the PAKE password
        let password = Password::new(self.invite_code.as_bytes());

        // Create SPAKE2 instance and get outbound message
        // Both sides must use the same idA and idB in the same order
        let (state, outbound_msg) = if is_initiator {
            Spake2::<Ed25519Group>::start_a(
                &password,
                &Identity::new(b"initiator"),
                &Identity::new(b"responder"),
            )
        } else {
            Spake2::<Ed25519Group>::start_b(
                &password,
                &Identity::new(b"initiator"), // idA - same as start_a
                &Identity::new(b"responder"), // idB - same as start_a
            )
        };

        let outbound_vec = outbound_msg.to_vec();

        // Store the SPAKE2 state for finish() call
        self.spake2_state = Some(if is_initiator {
            Spake2StateWrapper::Initiator(state)
        } else {
            Spake2StateWrapper::Responder(state)
        });

        Ok(outbound_vec)
    }

    /// Complete SPAKE2 exchange with peer's message
    /// Call this after receiving peer's message to derive shared secret
    #[allow(dead_code)]
    pub fn spake2_finish(&mut self, _is_initiator: bool, peer_msg: &[u8]) -> Result<Vec<u8>> {
        log::debug!(
            "Completing SPAKE2 exchange, peer_msg len: {}",
            peer_msg.len()
        );

        // Take the stored SPAKE2 state
        let state = self
            .spake2_state
            .take()
            .ok_or_else(|| anyhow::anyhow!("Must call spake2_start() first"))?;

        // Complete the exchange with peer's message using the stored state
        let shared_secret = match state {
            Spake2StateWrapper::Initiator(s) => s.finish(peer_msg),
            Spake2StateWrapper::Responder(s) => s.finish(peer_msg),
        }
        .map_err(|e| anyhow::anyhow!("SPAKE2 key exchange failed: {:?}", e))?;

        self.shared_secret = Some(shared_secret.clone());
        log::debug!(
            "SPAKE2 exchange complete, derived {} byte secret",
            shared_secret.len()
        );

        Ok(shared_secret)
    }

    /// Initialize Noise protocol handshake using SPAKE2 shared secret
    /// Uses HKDF to derive the Noise PSK for proper key hygiene
    #[allow(dead_code)]
    pub fn init_noise(&mut self, is_initiator: bool) -> Result<()> {
        log::debug!("Initializing Noise protocol (initiator: {})", is_initiator);

        // Ensure we have a shared secret from SPAKE2
        let shared_secret = self
            .shared_secret
            .as_ref()
            .ok_or_else(|| anyhow::anyhow!("Must complete SPAKE2 exchange first"))?;

        // Derive PSK using HKDF for proper key hygiene
        let psk = derive_noise_psk(shared_secret)?;
        log::debug!("Derived Noise PSK using HKDF-SHA256");

        // Use Noise_NNpsk0 pattern which doesn't require static keys
        // NN = no static keys, psk0 = PSK at beginning
        let params = "Noise_NNpsk0_25519_ChaChaPoly_BLAKE2b"
            .parse()
            .map_err(|e| anyhow::anyhow!("Invalid Noise pattern: {:?}", e))?;

        let builder = Builder::new(params);
        let builder = builder.psk(0, &psk); // Use derived PSK

        let noise_state = if is_initiator {
            builder
                .build_initiator()
                .map_err(|e| anyhow::anyhow!("Failed to build Noise initiator: {:?}", e))?
        } else {
            builder
                .build_responder()
                .map_err(|e| anyhow::anyhow!("Failed to build Noise responder: {:?}", e))?
        };

        self.noise_state = Some(NoiseState::Handshake(Box::new(noise_state)));
        log::debug!("Noise handshake initialized with HKDF-derived PSK");

        Ok(())
    }

    /// Process a Noise handshake message
    /// Captures handshake hash when transitioning to transport mode
    #[allow(dead_code)]
    pub fn noise_handshake_step(&mut self, message: &[u8]) -> Result<Option<Vec<u8>>> {
        let state = self
            .noise_state
            .take()
            .ok_or_else(|| anyhow::anyhow!("Noise not initialized"))?;

        match state {
            NoiseState::Handshake(mut hs) => {
                let mut buf = vec![0u8; 65535];
                let len = hs
                    .read_message(message, &mut buf)
                    .context("Noise handshake read failed")?;
                buf.truncate(len);

                // Check if handshake is complete
                if hs.is_handshake_finished() {
                    log::debug!("Noise handshake complete, transitioning to transport mode");

                    // Capture handshake hash before transitioning
                    let handshake_hash = hs.get_handshake_hash().to_vec();
                    self.handshake_hash = Some(handshake_hash);
                    log::debug!("Captured handshake hash for SAS derivation");

                    let transport = hs.into_transport_mode()?;
                    self.noise_state = Some(NoiseState::Transport(Box::new(transport)));
                    Ok(if len > 0 { Some(buf) } else { None })
                } else {
                    self.noise_state = Some(NoiseState::Handshake(hs));
                    Ok(if len > 0 { Some(buf) } else { None })
                }
            }
            NoiseState::Transport(_) => {
                anyhow::bail!("Handshake already complete")
            }
        }
    }

    /// Write a Noise handshake message
    /// Captures handshake hash when transitioning to transport mode
    #[allow(dead_code)]
    pub fn noise_handshake_write(&mut self) -> Result<Vec<u8>> {
        let state = self
            .noise_state
            .take()
            .ok_or_else(|| anyhow::anyhow!("Noise not initialized"))?;

        match state {
            NoiseState::Handshake(mut hs) => {
                let mut buf = vec![0u8; 65535];
                let len = hs
                    .write_message(&[], &mut buf)
                    .context("Noise handshake write failed")?;
                buf.truncate(len);

                // Check if handshake is complete
                if hs.is_handshake_finished() {
                    log::debug!(
                        "Noise handshake complete (after write), transitioning to transport mode"
                    );

                    // Capture handshake hash before transitioning
                    let handshake_hash = hs.get_handshake_hash().to_vec();
                    self.handshake_hash = Some(handshake_hash);
                    log::debug!("Captured handshake hash for SAS derivation");

                    let transport = hs.into_transport_mode()?;
                    self.noise_state = Some(NoiseState::Transport(Box::new(transport)));
                } else {
                    self.noise_state = Some(NoiseState::Handshake(hs));
                }

                Ok(buf)
            }
            NoiseState::Transport(ts) => {
                // Put state back before bailing
                self.noise_state = Some(NoiseState::Transport(ts));
                anyhow::bail!("Handshake already complete")
            }
        }
    }

    /// Encrypt a message using Noise transport
    /// TODO: Use for send() message encryption
    #[allow(dead_code)]
    pub fn encrypt(&mut self, plaintext: &[u8]) -> Result<Vec<u8>> {
        let state = self
            .noise_state
            .as_mut()
            .ok_or_else(|| anyhow::anyhow!("Noise not initialized"))?;

        match state {
            NoiseState::Transport(ts) => {
                let mut buf = vec![0u8; 65535];
                let len = ts
                    .write_message(plaintext, &mut buf)
                    .context("Noise encryption failed")?;
                buf.truncate(len);
                Ok(buf)
            }
            NoiseState::Handshake(_) => {
                anyhow::bail!("Handshake not complete")
            }
        }
    }

    /// Decrypt a message using Noise transport
    /// TODO: Use for recv() message decryption
    #[allow(dead_code)]
    pub fn decrypt(&mut self, ciphertext: &[u8]) -> Result<Vec<u8>> {
        let state = self
            .noise_state
            .as_mut()
            .ok_or_else(|| anyhow::anyhow!("Noise not initialized"))?;

        match state {
            NoiseState::Transport(ts) => {
                let mut buf = vec![0u8; 65535];
                let len = ts
                    .read_message(ciphertext, &mut buf)
                    .context("Noise decryption failed")?;
                buf.truncate(len);
                Ok(buf)
            }
            NoiseState::Handshake(_) => {
                anyhow::bail!("Handshake not complete")
            }
        }
    }

    /// Generate SAS (Short Authentication String) for user verification
    /// If Noise handshake is complete, derives SAS from handshake hash (55 bits entropy)
    /// Otherwise falls back to session_id + invite_code for backwards compatibility
    pub fn generate_sas(&self, session_id: &str) -> String {
        // Prefer handshake hash if available (after Noise handshake completion)
        if let Some(ref handshake_hash) = self.handshake_hash {
            log::debug!("Deriving SAS from Noise handshake hash");
            return derive_sas_from_transcript(handshake_hash);
        }

        // Fallback: derive from session_id and invite_code
        // This is used before handshake completes or in legacy scenarios
        log::debug!("Deriving SAS from session_id (handshake not complete)");
        let mut hasher = Sha256::new();
        hasher.update(session_id.as_bytes());
        hasher.update(self.invite_code.as_bytes());
        let hash = hasher.finalize();

        // Use same word extraction logic as derive_sas_from_transcript
        derive_sas_from_transcript(&hash)
    }

    /// Generate numeric SAS (6 digits)
    /// TODO: Use in session initialization
    #[allow(dead_code)]
    pub fn generate_sas_numeric(&self, session_id: &str) -> String {
        let mut hasher = Sha256::new();
        hasher.update(session_id.as_bytes());
        hasher.update(self.invite_code.as_bytes());
        let hash = hasher.finalize();

        // Take first 4 bytes and convert to 6-digit number
        let num = u32::from_be_bytes([hash[0], hash[1], hash[2], hash[3]]) % 1_000_000;
        format!("{:06}", num)
    }
}

